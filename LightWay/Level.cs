using System;

namespace LightWay
{
    /// <summary>
    /// Модель уровня. Хранит сетку 10×10, координаты источника
    /// и начальное направление луча. Содержит фабрику уровней.
    /// </summary>
    public class Level
    {
        public const int GridSize = 10;

        public Cell[,] Grid { get; private set; }

        // Координаты источника света
        public int SourceRow { get; private set; }
        public int SourceCol { get; private set; }

        // Начальное направление луча из источника
        public Direction StartDirection { get; private set; }

        /// <summary>
        /// Сколько раз можно повернуть зеркало за попытку. Если лимит исчерпан
        /// и луч ещё не в приёмнике — проигрыш.
        /// </summary>
        public int MaxMirrorClicks { get; private set; }

        /// <summary>
        /// Лимит времени в секундах. Ноль — таймер выключен.
        /// </summary>
        public int TimeLimitSeconds { get; private set; }

        public Level(Cell[,] grid, int sourceRow, int sourceCol, Direction startDirection,
            int maxMirrorClicks = 12, int timeLimitSeconds = 0)
        {
            Grid = grid;
            SourceRow = sourceRow;
            SourceCol = sourceCol;
            StartDirection = startDirection;
            MaxMirrorClicks = Math.Max(1, maxMirrorClicks);
            TimeLimitSeconds = Math.Max(0, timeLimitSeconds);
        }

        /// <summary>
        /// Сбросить подсветку всех клеток (перед пересчётом луча).
        /// </summary>
        public void ClearLit()
        {
            for (int r = 0; r < GridSize; r++)
                for (int c = 0; c < GridSize; c++)
                    Grid[r, c].IsLit = false;
        }

        // ========== Фабрика уровней ==========

        /// <summary>
        /// Загрузить уровень по номеру.
        /// Легенда массива:
        ///   0 = Empty, 1 = Source, 2 = Receiver,
        ///   3 = MirrorLeft (\), 4 = MirrorRight (/), 5 = Wall
        /// </summary>
        public static Level LoadLevel(int levelNumber)
        {
            switch (levelNumber)
            {
                case 1: return BuildLevel1();
                case 2: return BuildLevel2();
                case 3: return BuildLevel3();
                case 4: return BuildLevel4();
                case 5: return BuildLevel5();
                default: return BuildLevel1();
            }
        }

        public static int TotalLevels => 5;

        // -----------------------------------------------------------------------
        // Обучающий уровень — отдельный, не входит в основные 5.
        // -----------------------------------------------------------------------
        // Источник (0,0) → Right. Три зеркала нужно повернуть, две стены, одно
        // ложное зеркало в стороне. Запас по ходам и времени, чтобы успеть читать
        // подсказки.
        //
        // Правильный путь (3 поворота):
        //   Повернуть (0,3)/ → \, (5,3)/ → \, (5,7)/ → \
        //   Right → (0,3)\ → Down → (5,3)\ → Right → (5,7)\ → Down → Receiver(8,7)
        //
        // Ложный путь A — не трогать (0,3):
        //   Right → (0,3)/ → Up → выход за поле. Тупик сразу.
        //
        // Ложный путь B — повернуть (0,3)\, оставить (5,3)/:
        //   Down → (5,3)/ → Left → стена (5,1). Тупик у стены.
        //
        // Ложный путь C — повернуть (0,3)\, (5,3)\, оставить (5,7)/:
        //   Right → (5,7)/ → Up → стена (3,7). Тупик у стены.
        //
        // Ложное зеркало (2,5)\ — стоит в стороне, луч до него не доходит.
        //
        // Минимум ходов: 3. Лимит ходов: 8. Лимит времени: 120 секунд.
        // -----------------------------------------------------------------------
        public static Level LoadTutorial()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник
            map[5, 1] = 5;  // Стена — блокирует ложный путь B влево
            map[3, 7] = 5;  // Стена — блокирует ложный путь C вверх
            map[0, 3] = 4;  // / — нужно повернуть в \
            map[5, 3] = 4;  // / — нужно повернуть в \
            map[5, 7] = 4;  // / — нужно повернуть в \
            map[2, 5] = 3;  // \ — ложное зеркало в стороне от пути
            map[8, 7] = 2;  // Приёмник

            return BuildFromMap(map, 0, 0, Direction.Right, maxMirrorClicks: 8, timeLimitSeconds: 120);
        }

        // -----------------------------------------------------------------------
        // Уровень 1 — обучение: два зеркала, одно лишнее
        // -----------------------------------------------------------------------
        // Источник (0,0) → Right.
        //
        // Правильный путь (1 поворот):
        //   Повернуть (4,4)/ → \
        //   Right → (0,4)\ → Down → (4,4)\ → Right → Receiver(4,8)
        //
        // Ловушки для новичка:
        //   • Зеркало (0,7)/ стоит в той же строке что и Source — выглядит важным,
        //     но луч до него не доходит (отражается на (0,4)).
        //     Если игрок кликает (0,7) — тратит ход впустую.
        //   • Если не трогать (4,4) — луч уходит влево после (4,4)/ и теряется.
        //
        // Итог: нужно понять, что (0,4) уже стоит правильно, а трогать надо (4,4).
        // Минимум ходов: 1. Лимит: 6.
        // -----------------------------------------------------------------------
        private static Level BuildLevel1()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник, луч вправо
            map[0, 4] = 3;  // \ — уже правильно, не трогать
            map[0, 7] = 4;  // / — ложная приманка в той же строке
            map[4, 4] = 4;  // / — нужно повернуть в \
            map[4, 8] = 2;  // Приёмник

            return BuildFromMap(map, 0, 0, Direction.Right, maxMirrorClicks: 6, timeLimitSeconds: 0);
        }

        // -----------------------------------------------------------------------
        // Уровень 2 — первый обман: два пути, один заблокирован
        // -----------------------------------------------------------------------
        // Источник (0,0) → Right.
        //
        // Правильный путь (2 поворота):
        //   Повернуть (0,3)/ → \, повернуть (6,3)/ → \
        //   Right → (0,3)\ → Down → (6,3)\ → Right → Receiver(6,8)
        //
        // Ложный путь A — "прямо вправо":
        //   Не трогать (0,3) — луч идёт дальше вправо, упирается в стену (0,7). Тупик.
        //   Игрок видит стену и понимает: надо отклонить луч раньше.
        //
        // Ложный путь B — "вниз и влево":
        //   Повернуть (0,3)\ → Down, но оставить (6,3)/ →
        //   Down → (6,3)/ → Left → упирается в стену (6,1). Тупик.
        //   Игрок понимает: (6,3) тоже надо повернуть.
        //
        // Ложное зеркало (3,6)\ — стоит в середине поля, выглядит важным.
        //   Луч туда не доходит при правильном решении.
        //   Если игрок кликает — тратит ход.
        //
        // Минимум ходов: 2. Лимит: 8.
        // -----------------------------------------------------------------------
        private static Level BuildLevel2()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник
            map[0, 7] = 5;  // Стена — блокирует прямой путь вправо по строке 0
            map[6, 1] = 5;  // Стена — блокирует ложный путь влево по строке 6
            map[0, 3] = 4;  // / — нужно повернуть в \
            map[6, 3] = 4;  // / — нужно повернуть в \
            map[3, 6] = 3;  // \ — ложное зеркало, луч сюда не доходит
            map[6, 8] = 2;  // Приёмник

            return BuildFromMap(map, 0, 0, Direction.Right, maxMirrorClicks: 8, timeLimitSeconds: 45);
        }

        // -----------------------------------------------------------------------
        // Уровень 3 — три пути, два тупика
        // -----------------------------------------------------------------------
        // Источник (0,0) → Right.
        //
        // Правильный путь (3 поворота):
        //   Повернуть (0,2)/ → \, (3,2)/ → \, (3,6)/ → \
        //   Right → (0,2)\ → Down → (3,2)\ → Right → (3,6)\ → Down → Receiver(8,6)
        //
        // Ложный путь A — "прямо":
        //   Не трогать (0,2) — луч идёт вправо, упирается в стену (0,5). Тупик сразу.
        //
        // Ложный путь B — "вниз и влево":
        //   Повернуть (0,2)\, оставить (3,2)/ → Left → стена (3,0). Тупик.
        //
        // Ложный путь C — "вниз, вправо, но вверх":
        //   Повернуть (0,2)\, (3,2)\, оставить (3,6)/ → Up → выход вверх. Тупик.
        //
        // Ложное зеркало (7,8)/ — стоит в стороне от пути, выглядит как "финальный поворот".
        //   Луч идёт вниз по col=6 и попадает в Receiver(8,6) — до (7,8) не доходит.
        //   Если игрок кликает — тратит ход.
        //
        // Стены:
        //   (0,5), (0,6) — блокируют путь A
        //   (3,0) — блокирует путь B
        //
        // Минимум ходов: 3. Лимит: 9.
        // -----------------------------------------------------------------------
        private static Level BuildLevel3()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник
            map[0, 5] = 5;  // Стена — блокирует прямой путь вправо
            map[0, 6] = 5;  // Стена — усиливает блокировку
            map[3, 0] = 5;  // Стена — блокирует ложный путь влево
            map[0, 2] = 4;  // / — нужно повернуть в \
            map[3, 2] = 4;  // / — нужно повернуть в \
            map[3, 6] = 4;  // / — нужно повернуть в \
            map[7, 8] = 4;  // / — ложное зеркало в стороне от пути
            map[8, 6] = 2;  // Приёмник

            return BuildFromMap(map, 0, 0, Direction.Right, maxMirrorClicks: 9, timeLimitSeconds: 38);
        }

        // -----------------------------------------------------------------------
        // Уровень 4 — настоящее разветвление: каждое зеркало меняет маршрут
        // -----------------------------------------------------------------------
        // Источник (0,0) → Right.
        //
        // Правильный путь (3 поворота):
        //   Повернуть (0,3)/ → \, (4,3)/ → \, (4,7)/ → \
        //   Right → (0,3)\ → Down → (4,3)\ → Right → (4,7)\ → Down → Receiver(9,7)
        //
        // Ложный путь A — не трогать (0,3):
        //   Right → (0,3)/ → Up → выход за поле. Тупик сразу.
        //
        // Ложный путь B — повернуть (0,3)\, оставить (4,3)/:
        //   Down → (4,3)/ → Left → стена (4,1). Тупик.
        //
        // Ложный путь C — повернуть (0,3)\, (4,3)\, оставить (4,7)/:
        //   Right → (4,7)/ → Up → стена (2,7). Тупик.
        //   Игрок видит стену сверху — понимает, надо идти вниз, надо повернуть (4,7).
        //
        // Ложные зеркала:
        //   (7,3)\ — стоит ниже точки поворота (4,3), выглядит как "следующий шаг".
        //     После (4,3)\ луч идёт вправо, до (7,3) не доходит.
        //   (0,6)\ — стоит в строке источника, выглядит как ещё один поворот.
        //     После (0,3)\ луч уходит вниз, до (0,6) не доходит.
        //
        // Стены:
        //   (4,1) — блокирует ложный путь B влево
        //   (2,7) — блокирует ложный путь C вверх
        //
        // Минимум ходов: 3. Лимит: 9.
        // -----------------------------------------------------------------------
        private static Level BuildLevel4()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник
            map[4, 1] = 5;  // Стена — блокирует ложный путь влево
            map[2, 7] = 5;  // Стена — блокирует ложный путь вверх из (4,7)
            map[0, 3] = 4;  // / — нужно повернуть в \
            map[4, 3] = 4;  // / — нужно повернуть в \
            map[4, 7] = 4;  // / — нужно повернуть в \
            map[7, 3] = 3;  // \ — ложное зеркало ниже пути
            map[0, 6] = 3;  // \ — ложное зеркало в строке источника
            map[9, 7] = 2;  // Приёмник

            return BuildFromMap(map, 0, 0, Direction.Right, maxMirrorClicks: 9, timeLimitSeconds: 30);
        }

        // -----------------------------------------------------------------------
        // Уровень 5 — финал: путь по периметру через правый край
        // -----------------------------------------------------------------------
        // Источник (0,0) → Down. Приёмник (9,0).
        //
        // Правильный путь (4 поворота):
        //   Повернуть (2,0)/ → \, (2,7)/ → \, (6,7)/ → \, (6,9)/ → \
        //   (9,9)/ — НЕ трогать, уже правильно
        //
        //   (0,0)Down → (1,0) → (2,0)\ : Down→Right
        //   → (2,1)..(2,6) → (2,7)\ : Right→Down
        //   → (3,7)..(5,7) → (6,7)\ : Down→Right
        //   → (6,8) → (6,9)\ : Right→Down
        //   → (7,9),(8,9) → (9,9)/ : Down→Left
        //   → (9,8)..(9,1) → Receiver(9,0) ✓
        //
        // Ложные пути:
        //   • Не трогать (2,0): Down→Left → выход за поле слева. Тупик.
        //   • Тронуть (9,9): / → \, тогда Down→Right — выход за поле справа. Тупик.
        //   • Не трогать (2,7): Right→Up → (0,7) → выход вверх. Тупик.
        //   • Не трогать (6,7): Down→Left по row=6 → выход слева. Тупик.
        //   • Не трогать (6,9): Right→Up по col=9 → выход вверх. Тупик.
        //
        // Ложные зеркала (декорации, луч до них не доходит при правильном решении):
        //   (0,2)/, (1,4)/, (4,0)/, (4,4)/ — выглядят важными, но не нужны.
        //   Любой клик по ним — потерянный ход.
        //
        // Стены:
        //   (4,6), (8,5) — отсекают альтернативные идеи через центр / низ.
        //
        // Минимум поворотов: 4. Лимит: 12. Лимит времени: 30 секунд.
        // -----------------------------------------------------------------------
        private static Level BuildLevel5()
        {
            int[,] map = new int[GridSize, GridSize];

            map[0, 0] = 1;  // Источник, луч вниз
            map[9, 0] = 2;  // Приёмник

            map[4, 6] = 5;  // Стена
            map[8, 5] = 5;  // Стена

            map[0, 2] = 4;  // / — ложное зеркало (декорация)
            map[1, 4] = 4;  // / — ложное зеркало (декорация)
            map[2, 0] = 4;  // / — нужно повернуть в \
            map[2, 7] = 4;  // / — нужно повернуть в \
            map[4, 0] = 4;  // / — ложное зеркало (декорация)
            map[4, 4] = 4;  // / — ложное зеркало (декорация)
            map[6, 7] = 4;  // / — нужно повернуть в \
            map[6, 9] = 4;  // / — нужно повернуть в \
            map[9, 9] = 4;  // / — УЖЕ правильно, НЕ трогать

            return BuildFromMap(map, 0, 0, Direction.Down, maxMirrorClicks: 12, timeLimitSeconds: 30);
        }

        /// <summary>
        /// Собрать уровень из карты 10×10 (цифры как в комментариях к LoadLevel).
        /// Вынесено отдельно, чтобы удобно собирать поля в unit-тестах.
        /// </summary>
        public static Level FromDigitMap(int[,] map, int srcRow, int srcCol, Direction dir,
            int maxMirrorClicks = 10, int timeLimitSeconds = 0)
        {
            if (map.GetLength(0) != GridSize || map.GetLength(1) != GridSize)
                throw new ArgumentException("Карта должна быть размером 10×10.");

            var grid = new Cell[GridSize, GridSize];

            for (int r = 0; r < GridSize; r++)
            {
                for (int c = 0; c < GridSize; c++)
                {
                    CellType type = (CellType)map[r, c];
                    grid[r, c] = new Cell(r, c, type);
                }
            }

            return new Level(grid, srcRow, srcCol, dir, maxMirrorClicks, timeLimitSeconds);
        }

        private static Level BuildFromMap(int[,] map, int srcRow, int srcCol, Direction dir,
            int maxMirrorClicks, int timeLimitSeconds)
        {
            return FromDigitMap(map, srcRow, srcCol, dir, maxMirrorClicks, timeLimitSeconds);
        }
    }
}
